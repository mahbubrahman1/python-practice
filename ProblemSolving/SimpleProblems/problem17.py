
# Program for determining the area of a rectangle(আয়তক্ষেত্রের ক্ষেত্রফল নির্ণয়ের প্রোগ্রাম)

length = int(input("Enter length of rectangle: "))
width = int(input("Enter width of rectangle: "))
result = length * width   #আয়তক্ষেত্রের ক্ষেত্রফল = দৈর্ঘ্য ×প্রস্থ
print("The area of rectangle =", result)